#include "includes/libft.h"

int		ft_intlen(int n)
{
	int i;

	i = 0;
	if (n == 0)
		return (1);
	if (n < 0)
	{
		i = i + 2;
		if (n / 10 != 0)
			n = -(n / 10);
	}
	while (n > 0)
	{
		n = n / 10;
		i++;
	}
	return (i);
}
