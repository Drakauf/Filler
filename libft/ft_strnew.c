#include "includes/libft.h"

char	*ft_strnew(size_t size)
{
	size_t	i;
	char	*str;

	i = 0;
	if (!(str = (char *)malloc(sizeof(*str) * size + 1)))
		return (NULL);
	while (size > i)
	{
		str[i] = '\0';
		i++;
	}
	str[i] = '\0';
	return (str);
}
